function createData(){
   let id = document.getElementById("char1").value;
   let name = characterIds[parseInt(document.getElementById("char1").value, 16)];
   let availability = document.getElementById("avail").value;
   let recruitmentrequirements = document.getElementById("rec1").value;
   let recruitment = document.getElementById("rec2").value;
   let imageFile = document.getElementById("img1").value;

   let char = {name: name, id: id, img: imageFile, availability: availability, recruitment: recruitment, recruitmentrequirements: recruitmentrequirements};


   downloadObjectAsJson(char, name);

}

function downloadObjectAsJson(exportObj, exportName){
    var dataStr = "data:text/json;charset=utf-8," + encodeURIComponent(JSON.stringify(exportObj));
    var downloadAnchorNode = document.createElement('a');
    downloadAnchorNode.setAttribute("href",     dataStr);
    downloadAnchorNode.setAttribute("download", exportName + ".json");
    document.body.appendChild(downloadAnchorNode); // required for firefox
    downloadAnchorNode.click();
    downloadAnchorNode.remove();
}

